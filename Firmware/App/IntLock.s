  .syntax unified
  .cpu cortex-m3
  .fpu softvfp
  .thumb

.global intLock
.global intUnlock


  .section .text.intLock
  .type intLock, %function
intLock:
  mrs r0, PRIMASK
  cpsid i
  bx lr

  .section .text.intUnlock
  .type intlock, %function
intUnlock:
  msr PRIMASK, r0
  bx lr






